<?php
    function sum($t1, $t2){
        $sum = $t1 + $t2;
        return $sum;
    }
    
    function multiply($t1, $t2){
        $prod = $t1 * $t2;
        return $prod;
    }

    function division($t1, $t2){
        $div = $t1 / $t2; 
        return $div; 
    }

    function subtraction($t1, $t2){
        $dif = $t1 - $t2;
        return $dif;
    }

    ?>